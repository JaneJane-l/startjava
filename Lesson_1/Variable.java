public class Variable {
	public static void main(String[] args) {
		byte coresNum = 2; 
		short memory = 8000;
		long frequency = 2_500_000_000l;
		int typeSystem = 64;
		float availableMemory = 7.79f;
		double frequency2 = 2.5;
		char processor = 'i';
		boolean isWorking = true;

		System.out.println(coresNum);
		System.out.println(memory);
		System.out.println(frequency);
		System.out.println(typeSystem);
		System.out.println(availableMemory);
		System.out.println(frequency2);
		System.out.println(processor);
		System.out.println(isWorking);
	}
	
}